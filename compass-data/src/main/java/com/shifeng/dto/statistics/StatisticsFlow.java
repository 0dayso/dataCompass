package com.shifeng.dto.statistics;

public class StatisticsFlow {
	//统计数
	private String count;
	//时间（天/小时）
	private String date;
	
	
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
